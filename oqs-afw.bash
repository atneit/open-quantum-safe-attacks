_oqs-afw() {
    local i cur prev opts cmds
    COMPREPLY=()
    cur="${COMP_WORDS[COMP_CWORD]}"
    prev="${COMP_WORDS[COMP_CWORD-1]}"
    cmd=""
    opts=""

    for i in ${COMP_WORDS[@]}
    do
        case "${i}" in
            oqs-afw)
                cmd="oqs-afw"
                ;;
            
            attack)
                cmd+="__attack"
                ;;
            completions)
                cmd+="__completions"
                ;;
            frodo-kem1344aes)
                cmd+="__frodo__kem1344aes"
                ;;
            frodo-kem640aes)
                cmd+="__frodo__kem640aes"
                ;;
            help)
                cmd+="__help"
                ;;
            memcmp-frodo-baseline)
                cmd+="__memcmp__frodo__baseline"
                ;;
            memcmp-frodo-find-e)
                cmd+="__memcmp__frodo__find__e"
                ;;
            *)
                ;;
        esac
    done

    case "${cmd}" in
        oqs-afw)
            opts=" -h -V -l  --help --version --loglevel   attack completions help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 1 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                
                --loglevel)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                    -l)
                    COMPREPLY=($(compgen -f "${cur}"))
                    return 0
                    ;;
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        
        oqs__afw__attack)
            opts=" -h -V  --help --version   memcmp-frodo-baseline memcmp-frodo-find-e help"
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 2 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
            case "${prev}" in
                
                *)
                    COMPREPLY=()
                    ;;
            esac
            COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
            return 0
            ;;
        oqs__afw__attack__help)
            opts=" -h -V  --help --version  "
            if [[ ${cur} == -* || ${COMP_CWORD} -eq 3 ]] ; then
                COMPREPLY=( $(compgen -W "${opts}" -- "${cur}") )
                return 0
            fi
